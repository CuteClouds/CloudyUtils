package xyz.cuteclouds.utils.args;

import org.intellij.lang.annotations.MagicConstant;
import xyz.cuteclouds.utils.args.lexer.ArgLexer;
import xyz.cuteclouds.utils.args.lexer.Position;
import xyz.cuteclouds.utils.args.lexer.Token;
import xyz.cuteclouds.utils.args.tuples.*;

import java.util.LinkedList;
import java.util.List;

import static xyz.cuteclouds.utils.args.ParserOptions.*;
import static xyz.cuteclouds.utils.args.lexer.TokenType.*;

/**
 * <p>Parser that converts either a {@link String}, an {@link ArgLexer} or a {@link List}{@literal <}{@link Token}{@literal >} to a {@link Tuple}.</p>
 * <p><b>Summary of the {@link ArgParser} notation:</b></p>
 * <p>
 * <b>{@link Text}</b>: {@code smart text, "quoted example", 'single quotes', `markdown code-quote`, """raw quotes"""}
 * <br>Represents a piece of raw text.
 * <br>({@code \n, \r, \b, \t, \f} and {@code \u} surrogates are available for smart and simple quotes.)
 * <br>(if the text is quoted with multiple quotes, like {@code ""raw quote""} or {@code '''alternate'''}, text will not be escaped.)
 * <p>
 * <b>{@link Tuple}</b>: {@code { ... content ... }} and {@code ( ... content ... )} (smart tuples)
 * <br>Tuples are Lists of {@link Arg}s, which can be {@link Pair}s, Texts or other Tuples.
 * <br>A feature of Smart Tuples is unboxing itself if it contains a single {@link Text} or {@link Tuple} inside.
 * <p>
 * <b>{@link Pair}</b>: {@code key: value} and {@code key = value}
 * <br>Pairs can be used to associate a specific value (an {@link Arg}) to a name ({@link String}).
 * <br>(by default, a pair with a value of pair gets its value transformed into a singleton Tuple with the original value.)
 * <p>
 * <b>Implicit {@link Tuple}</b>: {@code attribute 1, attribute 2, attribute 3; attribute 4, attribute 5}
 * <br>If a semicolon is detected, all commas will automatically create implicit tuples.
 * <br>the above notation will produce the same result as: {@code (attribute 1, attribute 2, attribute 3), (attribute 4, attribute 5)}
 * <p>
 * <p>
 * <p><b>Examples of the {@link ArgParser} notation:</b></p>
 * <p>
 * Example 1: <pre>"key1": "value1", "key2": "value2", "attribute1", "attribute2"</pre>
 * Example 2: <pre>flags: (smartTuples: true, implicitTuples: true, rawPairs: true)</pre>
 * Example 3: <pre>role: (name: user, tier: 0)</pre>
 * Example 4: <pre>roles: user, visitor; privileges: admin, quick support</pre>
 *
 * @see ParserOptions#NO_SMART_TUPLES
 * @see ParserOptions#NO_IMPLICIT_TUPLES
 * @see ParserOptions#RAW_PAIRS
 * @see ArgParser#parse()
 */
public class ArgParser {
    private final boolean semicolons, smartTuples, implicitTuples, rawPairs;
    //yes, a LinkedList. why? because I need Queue capabilities.
    private final LinkedList<Token> tokens;

    /**
     * Parses the tokens, using the provided parsing options.
     *
     * @param tokens the tokens to be parsed.
     * @param flags  the parsing flags.
     * @see ParserOptions
     * @see ParserOptions#NO_SMART_TUPLES
     * @see ParserOptions#NO_IMPLICIT_TUPLES
     * @see ParserOptions#RAW_PAIRS
     * @see ArgParser#parse()
     */
    public ArgParser(List<Token> tokens, @MagicConstant(flagsFromClass = ParserOptions.class) int flags) {
        this.tokens = new LinkedList<>(tokens);

        this.semicolons = tokens.stream().anyMatch(token -> token.getType() == SEMICOLON);
        if (tokens.isEmpty()) tokens.add(new Token(new Position(0, 0, 0), EOF));

        this.smartTuples = (flags & NO_SMART_TUPLES) == 0;
        this.implicitTuples = (flags & NO_IMPLICIT_TUPLES) == 0;
        this.rawPairs = (flags & RAW_PAIRS) == RAW_PAIRS;

        //System.out.println(tokens.stream().map(Object::toString).collect(Collectors.joining(" ")));
    }

    /**
     * Parses the tokens generated by the lexer, using the provided parsing options.
     *
     * @param lexer the lexer which tokens will be parsed.
     * @param flags the parsing flags.
     * @see ParserOptions
     * @see ParserOptions#NO_SMART_TUPLES
     * @see ParserOptions#NO_IMPLICIT_TUPLES
     * @see ParserOptions#RAW_PAIRS
     * @see ArgParser#parse()
     */
    public ArgParser(ArgLexer lexer, @MagicConstant(flagsFromClass = ParserOptions.class) int flags) {
        this(lexer.getTokens(), flags);
    }

    /**
     * Creates a new parser, lexing the String first, and using the provided parsing options.
     *
     * @param s     the string to be parsed.
     * @param flags the parsing flags.
     * @throws SyntaxException if the string is not in a valid notation.
     * @see ParserOptions
     * @see ParserOptions#NO_SMART_TUPLES
     * @see ParserOptions#NO_IMPLICIT_TUPLES
     * @see ParserOptions#RAW_PAIRS
     * @see ArgParser#parse()
     */
    public ArgParser(String s, @MagicConstant(flagsFromClass = ParserOptions.class) int flags) {
        this(new ArgLexer(s), flags);
    }

    /**
     * Parses the tokens, using the default parsing options.
     *
     * @param tokens the tokens to be parsed.
     * @see ArgParser#parse()
     */
    public ArgParser(List<Token> tokens) {
        this(tokens, DEFAULT);
    }

    /**
     * Parses the tokens generated by the lexer, using the default parsing options.
     *
     * @param lexer the lexer which tokens will be parsed.
     * @see ArgParser#ArgParser(ArgLexer, int)
     * @see ArgParser#parse()
     */
    public ArgParser(ArgLexer lexer) {
        this(lexer, DEFAULT);
    }

    /**
     * Creates a new parser, lexing the String first, and using the default parsing options.
     *
     * @param s the string to be parsed.
     * @throws SyntaxException if the string is not in a valid notation.
     * @see ArgParser#ArgParser(String, int)
     * @see ArgParser#parse()
     */
    public ArgParser(String s) {
        this(s, DEFAULT);
    }

    /**
     * Parses the content to a {@link Tuple}.
     *
     * @return the result of the parsing
     * @throws SyntaxException if the string is not in a valid notation.
     * @see Arg
     * @see Tuple
     */
    public Tuple parse() {
        if (tokens.peek().is(EOF)) return new LinkedTuple();

        Tuple tuple = parseTuple(null, true, false);
        if (tuple.isSingleton()) {
            Arg arg = tuple.firstArg();
            if (arg.isTuple()) return arg.asTuple();
        }

        return tuple;
    }

    private Arg parseOnce(boolean root) {
        Token token = tokens.poll();

        switch (token.getType()) {
            case LINE: {
                return parseOnce(root);
            }
            case LEFT_PAREN: {
                return parseSmartTuple();
            }
            case LEFT_BRACE: {
                return parseTuple(null, false, false);
            }
            case TEXT: {
                String text = token.getString();

                if (tokens.peek().is(ASSIGN)) {
                    tokens.poll();

                    Arg value = parseOnce(false);

                    if (root && semicolons && implicitTuples && tokens.peek().is(COMMA)) {
                        tokens.poll();
                        value = parseTuple(value, false, false);
                    }

                    if (value.isPair() && !rawPairs) {
                        value = new LinkedTuple(value);
                    }

                    return new Pair(text, value);
                } else {
                    Arg arg = new Text(text);

                    if (root && semicolons && tokens.peek().is(COMMA)) {
                        tokens.poll();
                        return parseTuple(arg, false, false);
                    }

                    return arg;
                }
            }
        }

        throw new SyntaxException("Unexpected " + token, token.getPosition());
    }

    private Arg parseSmartTuple() {
        Tuple tuple = parseTuple(null, false, true);
        if (smartTuples && tuple.isSingleton()) {
            Arg arg = tuple.firstArg();
            if (!arg.isPair() && !rawPairs) return arg;
        }
        return tuple;
    }

    private Tuple parseTuple(Arg first, boolean root, boolean smart) {
        Tuple tuple = new LinkedTuple();

        boolean implicit = first != null;

        assert !(implicit && root && smart);

        if (implicit) {
            tuple.add(first);
        }

        if (!implicit && !root && tokens.peek().is(smart ? RIGHT_PAREN : RIGHT_BRACE)) {
            tokens.poll();
            return tuple;
        }

        while (true) {
            tuple.add(parseOnce(!implicit));

            Token token = tokens.poll();
            switch (token.getType()) {
                case RIGHT_PAREN: {
                    if (implicit) {
                        tokens.push(token);
                        return tuple;
                    }
                    if (!root && smart) return tuple;
                    break;
                }

                case RIGHT_BRACE: {
                    if (implicit) {
                        tokens.push(token);
                        return tuple;
                    }
                    if (!root && !smart) return tuple;
                    break;
                }

                case COMMA: {
                    if (root && semicolons && implicitTuples) break;
                    continue;
                }

                case LINE:
                case SEMICOLON: {
                    if (implicit) {
                        tokens.push(token);
                        return tuple;
                    }

                    if (tokens.peek().is(EOF)) {
                        return tuple;
                    }
                    continue;
                }
                case EOF: {
                    tokens.push(token);
                    if (!implicit && !root) break;
                    return tuple;
                }
            }

            throw new SyntaxException("Unexpected " + token, token.getPosition());
        }
    }
}
